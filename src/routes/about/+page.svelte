      <div class="divide-y divide-gray-200">
        <div class="px-4 py-5 sm:px-6">
          <article class="prose max-w-none">
            <h2>Rules</h2>

            <h3>Platform</h3>

            <ul class="section-text">
              <li>
                You must play on PC, so that other people can replay your seeds
                to verify your runs. (For example, replaying a seed from the
                Nintendo Switch on PC would result in a completely different
                layout.)
              </li>
              <li>
                You must play on the latest version of the game, so that other
                people can easily replay your seeds to verify your runs. (For
                example, reverting your game to a previous patch before
                something was changed is not allowed.)
              </li>
              <li>
                Every run must be streamed live on
                <a
                  href="https://www.twitch.tv"
                  target="_blank"
                  rel="noopener noreferrer">Twitch</a
                >
                or
                <a
                  href="https://www.youtube.com/"
                  target="_blank"
                  rel="noopener noreferrer">YouTube</a
                >. (For example, showing video recordings of your run after the
                fact does not count.) You must also
                <a
                  href="https://streamersplaybook.com/how-to-enable-vod-on-twitch/"
                  >enable VODs</a
                > so that administrators can review your runs after they are finished.
              </li>
            </ul>

            <div class="mb-8" />

            <h3>Before the Run</h3>

            <ul class="section-text">
              <li>Every run must be played on the "hard" difficulty.</li>
              <li>
                Every run must be played on a fully-unlocked save file (i.e.
                Dead God).
              </li>
              <li>
                Any mods that change gameplay are forbidden. (For example, mods
                that add new items, add new rooms, or change vanilla mechanics.)

                <ul>
                  <li>
                    Mods that are only cosmetic are allowed. (For example,
                    sprite tweaks.)
                  </li>
                  <li>
                    Mods that make lucky pennies and tinted rocks easier to see
                    are allowed, since they count as cosmetic mods.
                  </li>
                  <li>
                    Mods that swap the sound effects and/or music are allowed,
                    since they count as cosmetic mods.
                  </li>
                  <li>
                    <a
                      href="https://steamcommunity.com/sharedfiles/filedetails/?id=1978904635"
                      >MinimapAPI</a
                    > is allowed, since it counts as a cosmetic mod. (Note that you
                    must configure MinimapAPI in a way that is comparable to the
                    vanilla minimap, such that you do not get any gameplay-related
                    advantage.)
                  </li>
                  <li>
                    The <a
                      href="https://steamcommunity.com/sharedfiles/filedetails/?id=2489006943"
                      >Planetarium Chance</a
                    > mod is allowed, since it counts as a cosmetic mod.
                  </li>
                  <li>
                    The <a
                      href="https://steamcommunity.com/sharedfiles/filedetails/?id=2617557401"
                      >Time Machine</a
                    > mod is allowed, since it does not alter gameplay.
                  </li>
                  <li>
                    Mods that skip "giantbook" animations such as Whore of
                    Babylon are not allowed, since some people consider them to
                    affect gameplay in the sense that they interrupt the tempo
                    of having to dodge difficult boss patterns.
                  </li>
                </ul>
              </li>
              <li>
                When your streak starts, the donation machine in the shop can be
                at any value between 0 and 999. However, once the streak starts,
                you are not allowed to change this value in any way other than
                by donating coins to the machine and bombing the machine. (For
                example, you are not allowed to edit the save file in manually
                change the value.) You are allowed to bomb the donation machine
                in the shop in order to get coins that were donated in a
                previous run.
              </li>
              <li>
                You must start the run as the character from the category you
                are playing. If you accidentally start a run as a different
                character from the streak you are working on, you are allowed to
                exit the game and restore a previously backed up save file in
                order to preserve the streak count.
              </li>
              <li>
                You are allowed to change any value inside the "options.ini" and
                "config.ini" files. (Not all of the configurable values are
                properly exposed in the in-game menus, so editing the files
                manually is needed to enable some options, such as mouse
                control.)
              </li>
              <li>
                You are not allowed to use Cheat Engine, replace the game files,
                or use any other method of modifying the game.
              </li>
            </ul>

            <div class="mb-8" />

            <h3>During the Run</h3>

            <ul class="section-text">
              <li>
                You must trigger the ending cinematic/cutscene of the goal for
                the category you are playing. (For example, if you complete the
                goal but die before jumping into the chest, your streak is not
                incremented and it is counted the same as if you died to the
                boss.)

                <ul>
                  <li>
                    Ending a run at Cathedral or Sheol is not allowed. Your
                    streak is reset to 0 in this case.
                  </li>
                  <li>
                    Ending a run by skipping the main goal in order to do a
                    harder goal like Delirium is not allowed. Your streak is
                    reset to 0 in this case.
                  </li>
                </ul>
              </li>
              <li>
                You are allowed to end the run on another character than the one
                you started with. (For example, you are allowed to take Judas'
                Shadow and revive as Dark Judas.)
              </li>
              <li>
                You are not allowed to pause the game in order to gain any type
                of gameplay advantage. (For example, you are not allowed to
                pause in the middle of a fight to think about what to do next,
                go back to the menu to escape damage, reset the Holy Mantle,
                stop a Restock Machine from "double rolling", select
                collectibles more easily with Glitched Crown, and so on.)

                <ul>
                  <li>
                    You are allowed to save and quit and continue if it the only
                    way to recover from a soft-lock.
                  </li>
                </ul>
              </li>
              <li>
                All runs should have the seeds visible on the screen, so that
                they can be replayed to verify if you are cheating. (We
                recommend using the <a
                  href="https://github.com/Rchardon/RebirthItemTracker"
                  target="_blank"
                  rel="noopener noreferrer"
                >
                  Rebirth Item Tracker
                </a> for this purpose. Otherwise, you can pause at the beginning
                or at the end of your run.)
              </li>
              <li>
                If you are streaking as Eden and you get TMTRAINER as your
                random starting passive item, you are allowed to skip the run
                without losing your streak. (However, this rule does not apply
                if the TMTRAINER was granted by an Eden's Blessing or a Genesis.
                If that was the case, then you have to continue the run.)
              </li>
              <li>
                You are not allowed to use the in-game console, with two
                exceptions:

                <ul>
                  <li>
                    Sometimes, you can accidentally take The Polaroid or The
                    Negative in the middle of a streak that requires the other
                    photo. In this case, you are allowed to use the debug
                    console to remove the photo that you took and manually add
                    the photo that you need. For example, <code
                      >remove negative</code
                    >
                    and
                    <code>giveitem polaroid</code>.
                  </li>
                  <li>
                    Sometimes, the game can get soft-locked in a situation where
                    it was not the fault of the player. Most of the time, this
                    can be fixing by saving and quitting, but other times, even
                    that will not help. For example, in some floor layouts,
                    using a Reverse Moon card to teleport to the Ultra Secret
                    Room results in a soft-lock due to the Ultra Secret Room not
                    having any doors. In this situation, it is allowed to use
                    the console to give yourself a Fool card (e.g. `giveitem
                    k1`). More generically, the player is allowed to use the
                    console to get themselves out of a soft-lock which was not
                    their fault. The moderators reserve the right to arbitrarily
                    determine which specific soft-lock situations warrant
                    console usage.
                  </li>
                </ul>
              </li>
              <li>
                You are not allowed to look up the recipes for Bag of Crafting
                corresponding to the seed of your run.
              </li>
            </ul>
